    private void jButtonFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFiltrarActionPerformed
        PilotoTableAndComboModel ccm=(PilotoTableAndComboModel)this.jComboBoxPiloto.getModel();
        Piloto piloto= ccm.getRow(this.jComboBoxPiloto.getSelectedIndex());
        List<Circuito> circuitos=((CircuitoModel)getModel()).listarPorPuntuados(piloto);
        circuitoTableModel.setCircuitos(circuitos);
    }//GEN-LAST:event_jButtonFiltrarActionPerformed

