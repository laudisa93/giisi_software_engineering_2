    public void refresh() {
        circuitoTableModel.setCircuitos(getModel().listar());//cambia el modelo de JTable(TableModel) por medio de una clase que hemos creado (ContactosTableModel)
    }

    protected void fireNuevaFacturaGesture(String nombre, String ubicacion, Piloto primero, Piloto segundo, Piloto tercero){
        List<Serializable> datos = new ArrayList<Serializable>();
        datos.add(nombre);
        datos.add(ubicacion);
        datos.add(primero);
        datos.add(segundo);
        datos.add(tercero);
        getController().nuevaEntidadGesture(datos);
    }

    protected void fireActualizaFacturaGesture(String nombre, String ubicacion, Piloto primero, Piloto segundo, Piloto tercero) {
        List<Serializable> datos = new ArrayList<Serializable>();
        datos.add(nombre);
        datos.add(ubicacion);
        datos.add(primero);
        datos.add(segundo);
        datos.add(tercero);
        getController().actualizaEntidadGesture(datos);
    }

    protected void fireBorraFacturaGesture(String identificador) {
        getController().borraEntidadGesture(identificador);
    }
